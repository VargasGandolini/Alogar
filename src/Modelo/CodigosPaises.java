package Modelo;

public class CodigosPaises {
    int id;
    String codigo;
    String pais;

    public CodigosPaises(int id, String codigo, String pais) {
        this.id = id;
        this.codigo = codigo;
        this.pais = pais;
    }

    public CodigosPaises() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }
    
    
}
