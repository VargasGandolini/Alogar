    public void ListarVentas() {
        List<Venta> ListarVenta = VDao.ListarVentas();
        modelo = (DefaultTableModel) TablaVentas.getModel();
        Object[] ob = new Object[3];
        for (int i = 0; i < ListarVenta.size(); i++) {
            ob[0] = ListarVenta.get(i).getId();
            ob[1] = ListarVenta.get(i).getTotal();
            ob[2] = ListarVenta.get(i).getFecha_venta();
            modelo.addRow(ob);
        }
        TablaVentas.setModel(modelo);
    }
    
    public void ListarProductosVentas() {
        List<ProductosVentas> ListarProductosVentas = VDao.ListarProductosVentas();
        modelo = (DefaultTableModel) TableProductosVentas.getModel();
        Comparator<ProductosVentas> cantidadComparator = Comparator.comparingInt(ProductosVentas::getCantidad).reversed();
        ListarProductosVentas.sort(cantidadComparator);
        modelo.setRowCount(0);
        Object[] ob = new Object[3];
        for (int i = 0; i < ListarProductosVentas.size(); i++) {
            ob[0] = ListarProductosVentas.get(i).getProductos();
            ob[1] = ListarProductosVentas.get(i).getCantidad();
            ob[2] = ListarProductosVentas.get(i).getPrecio_producto();
            modelo.addRow(ob);
        }
        TableProductosVentas.setModel(modelo);
    }

