    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    try {
        int id = Integer.parseInt(txtIdVentas.getText());
        File file = new File(System.getProperty("user.home") + "/Escritorio/Boletas/boleta" + id + ".pdf");
        PDDocument document = PDDocument.load(file);
        PrinterJob job = PrinterJob.getPrinterJob();

        // Obtener todas las impresoras disponibles en el sistema
        PrintService[] printServices = PrintServiceLookup.lookupPrintServices(null, null);
        PrintService namedPrinter = null;
        for (PrintService service : printServices) {
            if (service.getName().equals("HP_Smart_Tank_530_series_1FC5D6@HP6C02E01FC5D6.local")) {
                namedPrinter = service;
                break;
            }
        }

        if (namedPrinter != null) {
            // Configurar la impresora para la impresión
            job.setPrintService(namedPrinter);
            job.setPageable(new PDFPageable(document));
            job.print();
        } else {
            System.out.println("No se encontró la impresora con el nombre especificado.");
        }

        document.close();
    } catch (IOException | PrinterException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton4ActionPerformed

